package practice;

public class offers {

    public int creditpt;
    public int discount;
	public offers(int creditpt, int discount) {
		super();
		this.creditpt = creditpt;
		this.discount = discount;
	}
	public int getCreditpt() {
		return creditpt;
	}
	public void setCreditpt(int creditpt) {
		this.creditpt = creditpt;
	}
	public int getDiscount() {
		return discount;
	}
	public void setDiscount(int discount) {
		this.discount = discount;
	}
    
	
}
